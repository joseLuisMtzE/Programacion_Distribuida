public class Rectangle2 {
    // DECLARACION DE VARIABLES DE LA CLASE
    int width = 0;
    int height = 0;
    float area = 0;

    // CONSTRUCTOR DE CLASE
    public Rectangle2() {

    }

    // METODO PARA OBTENER EL AREA
    public float getArea() {
        area = width * height;
        return area;
    }
}

